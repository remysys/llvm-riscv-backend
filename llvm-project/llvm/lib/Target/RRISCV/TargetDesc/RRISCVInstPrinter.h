#ifndef LLVM_LIB_TARGET_RRISCV_MCTARGETDESC_RRISCVINSTPRINTER_H
#define LLVM_LIB_TARGET_RRISCV_MCTARGETDESC_RRISCVINSTPRINTER_H

#include <llvm/MC/MCInstPrinter.h>
#include <llvm/MC/MCRegister.h>
#include <llvm/Support/raw_ostream.h>

namespace llvm {
class RRISCVInstPrinter : public MCInstPrinter {
public:
  RRISCVInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                    const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(MCInst const *MI, uint64_t Address, StringRef Annot,
                 MCSubtargetInfo const &STI, raw_ostream &O) override;
  void printRegName(raw_ostream &O, unsigned RegNo) const override;

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printOperand(const MCInst *MI, uint64_t _Address, unsigned OpNum,
                    raw_ostream &O);

  // Autogenerated by tblgen. defined in RRISCVGenAsmWriter.inc
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  static const char *getRegisterName(unsigned RegNo, unsigned AltIdx);
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_RRISCV_MCTARGETDESC_RRISCVINSTPRINTER_H